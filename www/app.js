const storageKey = "normalChristianLifeStudy";
const state = loadState();
let activeChapter = Number(state.activeChapter || 0);
let activeTab = "memory";
let activeLanguage = ["en", "zh", "both"].includes(state.language) ? state.language : "both";
let quizAnswered = false;
let deferredInstallPrompt = null;

const chapterList = document.querySelector("#chapterList");
const chapterPanel = document.querySelector("#chapterPanel");
const progressText = document.querySelector("#progressText");
const memoryText = document.querySelector("#memoryText");
const searchInput = document.querySelector("#searchInput");
const memoryPane = document.querySelector("#memoryPane");
const quizPane = document.querySelector("#quizPane");
const notes = document.querySelector("#chapterNotes");
const saveStatus = document.querySelector("#saveStatus");
const installApp = document.querySelector("#installApp");
const languageButtons = document.querySelectorAll(".language-option");

function loadState() {
  try {
    return JSON.parse(localStorage.getItem(storageKey)) || {};
  } catch {
    return {};
  }
}

function saveState() {
  localStorage.setItem(storageKey, JSON.stringify(state));
}

function chapterKey(index) {
  return `chapter-${index}`;
}

function currentUi() {
  return activeLanguage === "zh" ? ui.zh : ui.en;
}

function textPair(en, zh, className = "") {
  if (activeLanguage === "en") return `<span class="${className}">${en}</span>`;
  if (activeLanguage === "zh") return `<span class="${className}" lang="zh-Hant">${zh || en}</span>`;
  return `
    <span class="${className} lang-en">${en}</span>
    <span class="${className} lang-zh" lang="zh-Hant">${zh || en}</span>
  `;
}

function plainPair(en, zh) {
  if (activeLanguage === "zh") return zh || en;
  if (activeLanguage === "both") return `${en} ${zh || ""}`;
  return en;
}

function localChapter(index) {
  return {
    en: chapters[index],
    zh: chapterZh[index] || {}
  };
}

function setStaticLanguage() {
  const labels = currentUi();
  document.documentElement.lang = activeLanguage === "zh" ? "zh-Hant" : "en";
  document.querySelector(".search span").textContent = labels.search;
  searchInput.placeholder = labels.searchPlaceholder;
  document.querySelector(".eyebrow").textContent = labels.eyebrow;
  document.querySelector(".hero-overlay p:not(.eyebrow)").textContent = labels.heroText;
  document.querySelector("#startStudy").textContent = labels.startStudy;
  document.querySelector("#reviewMemory").textContent = labels.reviewCards;
  document.querySelectorAll(".topbar .stat-label")[0].textContent = labels.progress;
  document.querySelectorAll(".topbar .stat-label")[1].textContent = labels.memory;
  document.querySelectorAll(".topbar .stat-label")[2].textContent = labels.focus;
  installApp.textContent = labels.install;
  document.querySelector('[data-tab="memory"]').textContent = labels.memory;
  document.querySelector('[data-tab="quiz"]').textContent = labels.quiz;
  document.querySelector('[data-tab="notes"]').textContent = labels.notes;
  document.querySelector(".notes-label").textContent = labels.personalNotes;
  notes.placeholder = labels.notesPlaceholder;
  document.querySelector("#saveNotes").textContent = labels.saveNotes;
  languageButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.language === activeLanguage);
  });
}

function renderChapterList(filter = "") {
  const query = filter.trim().toLowerCase();
  chapterList.innerHTML = "";
  chapters.forEach((chapter, index) => {
    const zh = chapterZh[index] || {};
    const haystack = [
      chapter.title,
      chapter.theme,
      chapter.summary,
      zh.title,
      zh.theme,
      zh.summary,
      chapter.verses.join(" "),
      chapter.concepts.map(([title, text]) => `${title} ${text}`).join(" "),
      zh.concepts?.map(([title, text]) => `${title} ${text}`).join(" "),
      chapter.verses.map((verse) => `${verseContent[verse] || ""} ${verseContentZh[verse] || ""}`).join(" ")
    ]
      .join(" ")
      .toLowerCase();

    if (query && !haystack.includes(query)) return;

    const button = document.createElement("button");
    button.className = `chapter-button ${index === activeChapter ? "active" : ""} ${state.completed?.[chapterKey(index)] ? "complete" : ""}`;
    button.innerHTML = `
      <span class="chapter-number">${index + 1}</span>
      <span class="chapter-title">${textPair(chapter.title, zh.title)}</span>
      <span class="done-dot" aria-hidden="true"></span>
    `;
    button.addEventListener("click", () => {
      activeChapter = index;
      state.activeChapter = index;
      saveState();
      quizAnswered = false;
      render();
      document.querySelector(".reader-layout").scrollIntoView({ behavior: "smooth", block: "start" });
    });
    chapterList.appendChild(button);
  });
}

function renderChapter() {
  const { en: chapter, zh } = localChapter(activeChapter);
  const labels = currentUi();
  chapterPanel.innerHTML = `
    <span class="chapter-kicker">${activeLanguage === "zh" ? `${labels.chapter}${activeChapter + 1}章` : `${labels.chapter} ${activeChapter + 1}`}</span>
    <h2>${textPair(chapter.title, zh.title)}</h2>
    <p class="chapter-summary">${textPair(chapter.summary, zh.summary)}</p>

    <div class="concept-grid">
      ${chapter.concepts
        .map(([title, text], index) => `
          <div class="concept-card">
            <strong>${textPair(title, zh.concepts?.[index]?.[0])}</strong>
            <p>${textPair(text, zh.concepts?.[index]?.[1])}</p>
          </div>
        `)
        .join("")}
    </div>

    <h3 class="section-title">${labels.practice}</h3>
    <ul class="practice-list">
      ${chapter.practices.map((item, index) => `<li>${textPair(item, zh.practices?.[index])}</li>`).join("")}
    </ul>

    <h3 class="section-title">${labels.verseAnchors}</h3>
    <ul class="verse-list">
      ${chapter.verses
        .map((item) => `
          <li>
            <strong>${item}</strong>
            <span>${textPair(verseContent[item] || "Read this passage as an anchor for the chapter theme.", verseContentZh[item] || "閱讀這段經文，作為本章主題的錨點。")}</span>
          </li>
        `)
        .join("")}
    </ul>

    <div class="reader-actions">
      <button class="primary-action" id="markComplete">${state.completed?.[chapterKey(activeChapter)] ? labels.markUnread : labels.markComplete}</button>
      <button class="secondary-action" id="previousChapter">${labels.previous}</button>
      <button class="secondary-action" id="nextChapter">${labels.next}</button>
    </div>
  `;

  document.querySelector("#markComplete").addEventListener("click", toggleComplete);
  document.querySelector("#previousChapter").addEventListener("click", () => moveChapter(-1));
  document.querySelector("#nextChapter").addEventListener("click", () => moveChapter(1));
}

function renderMemory() {
  const { en: chapter, zh } = localChapter(activeChapter);
  const labels = currentUi();
  const remembered = Boolean(state.memory?.[chapterKey(activeChapter)]);
  memoryPane.innerHTML = `
    <div class="memory-card" id="memoryCard">
      <p class="memory-front">${textPair(chapter.memory.front, zh.memory?.front)}</p>
      <p class="memory-back">${textPair(chapter.memory.back, zh.memory?.back)}</p>
    </div>
    <div class="tool-actions">
      <button class="primary-action" id="showMemory">${labels.showAnswer}</button>
      <button class="small-action" id="knownMemory">${remembered ? labels.needsReview : labels.knowThis}</button>
    </div>
  `;

  document.querySelector("#showMemory").addEventListener("click", () => {
    document.querySelector("#memoryCard").classList.add("revealed");
  });
  document.querySelector("#knownMemory").addEventListener("click", () => {
    state.memory = state.memory || {};
    state.memory[chapterKey(activeChapter)] = !remembered;
    saveState();
    render();
  });
}

function renderQuiz() {
  const { en: chapter, zh } = localChapter(activeChapter);
  quizPane.innerHTML = `
    <p class="quiz-question">${textPair(chapter.quiz.question, zh.quiz?.question)}</p>
    <div id="quizChoices">
      ${chapter.quiz.choices
        .map((choice, index) => `<button class="choice" data-choice="${index}">${textPair(choice, zh.quiz?.choices?.[index])}</button>`)
        .join("")}
    </div>
    <p class="quiz-result" id="quizResult"></p>
  `;

  document.querySelectorAll(".choice").forEach((choice) => {
    choice.addEventListener("click", () => answerQuiz(Number(choice.dataset.choice)));
  });
}

function answerQuiz(choiceIndex) {
  if (quizAnswered) return;
  quizAnswered = true;
  const { en: chapter, zh } = localChapter(activeChapter);
  const labels = currentUi();
  document.querySelectorAll(".choice").forEach((choice) => {
    const index = Number(choice.dataset.choice);
    if (index === chapter.quiz.answer) choice.classList.add("correct");
    if (index === choiceIndex && index !== chapter.quiz.answer) choice.classList.add("wrong");
  });
  document.querySelector("#quizResult").textContent =
    choiceIndex === chapter.quiz.answer
      ? `${labels.correct} ${plainPair(chapter.quiz.explanation, zh.quiz?.explanation)}`
      : `${labels.reviewMoment} ${plainPair(chapter.quiz.explanation, zh.quiz?.explanation)}`;
}

function renderNotes() {
  notes.value = state.notes?.[chapterKey(activeChapter)] || "";
  saveStatus.textContent = "";
}

function updateStats() {
  const completed = Object.values(state.completed || {}).filter(Boolean).length;
  const remembered = Object.values(state.memory || {}).filter(Boolean).length;
  const labels = currentUi();
  progressText.textContent = `${completed} / ${chapters.length}`;
  memoryText.textContent =
    activeLanguage === "zh"
      ? `${chapters.length - remembered} ${labels.cardsDue}`
      : `${chapters.length - remembered} ${labels.cardsDue}`;
}

function toggleComplete() {
  state.completed = state.completed || {};
  const key = chapterKey(activeChapter);
  state.completed[key] = !state.completed[key];
  saveState();
  render();
}

function moveChapter(direction) {
  activeChapter = Math.max(0, Math.min(chapters.length - 1, activeChapter + direction));
  state.activeChapter = activeChapter;
  quizAnswered = false;
  saveState();
  render();
}

function switchTab(tab) {
  activeTab = tab;
  document.querySelectorAll(".tab").forEach((button) => {
    button.classList.toggle("active", button.dataset.tab === tab);
  });
  document.querySelectorAll(".tool-pane").forEach((pane) => {
    pane.classList.toggle("active", pane.id === `${tab}Pane`);
  });
}

function render() {
  setStaticLanguage();
  renderChapterList(searchInput.value);
  renderChapter();
  renderMemory();
  renderQuiz();
  renderNotes();
  updateStats();
  switchTab(activeTab);
}

document.querySelectorAll(".tab").forEach((button) => {
  button.addEventListener("click", () => switchTab(button.dataset.tab));
});

document.querySelector("#saveNotes").addEventListener("click", () => {
  state.notes = state.notes || {};
  state.notes[chapterKey(activeChapter)] = notes.value;
  saveState();
  saveStatus.textContent = currentUi().saved;
});

searchInput.addEventListener("input", () => renderChapterList(searchInput.value));

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    activeLanguage = button.dataset.language;
    state.language = activeLanguage;
    saveState();
    render();
  });
});

document.querySelector("#startStudy").addEventListener("click", () => {
  document.querySelector(".reader-layout").scrollIntoView({ behavior: "smooth", block: "start" });
});

document.querySelector("#reviewMemory").addEventListener("click", () => {
  const firstDue = chapters.findIndex((_, index) => !state.memory?.[chapterKey(index)]);
  activeChapter = firstDue >= 0 ? firstDue : 0;
  activeTab = "memory";
  state.activeChapter = activeChapter;
  saveState();
  render();
  document.querySelector(".study-panel").scrollIntoView({ behavior: "smooth", block: "start" });
});

document.querySelector("#focusToggle").addEventListener("click", () => {
  document.body.classList.toggle("focus-mode");
});

window.addEventListener("beforeinstallprompt", (event) => {
  event.preventDefault();
  deferredInstallPrompt = event;
  installApp.hidden = false;
});

installApp.addEventListener("click", async () => {
  if (!deferredInstallPrompt) return;
  deferredInstallPrompt.prompt();
  await deferredInstallPrompt.userChoice;
  deferredInstallPrompt = null;
  installApp.hidden = true;
});

window.addEventListener("appinstalled", () => {
  deferredInstallPrompt = null;
  installApp.hidden = true;
});

if ("serviceWorker" in navigator && ["http:", "https:"].includes(location.protocol)) {
  window.addEventListener("load", () => {
    navigator.serviceWorker.register("./sw.js");
  });
}

render();
